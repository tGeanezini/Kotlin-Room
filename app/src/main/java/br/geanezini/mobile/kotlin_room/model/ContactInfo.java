package br.geanezini.mobile.kotlin_room.model;

public class ContactInfo {

    public String email;
    public String phone;

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }
}
